#!/usr/bin/env python3
"""Regenerate tasks/QUEUE.md from task-file frontmatter.

Usage: python3 tools/build_queue.py
Stdlib only. Parses the flat-YAML frontmatter described in tasks/README.md.
"""

import re
import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
TASKS = ROOT / "tasks"
QUEUE = TASKS / "QUEUE.md"

STATUS_ICON = {
    "pending": " ",
    "in-progress": "~",
    "implemented": "?",
    "refuted": "!",
    "verified": "x",
}


def parse_frontmatter(path: Path) -> dict | None:
    text = path.read_text(encoding="utf-8")
    m = re.match(r"\A---\n(.*?)\n---\n", text, re.DOTALL)
    if not m:
        return None
    fm: dict = {"_path": path}
    for line in m.group(1).splitlines():
        if ":" not in line:
            continue
        key, _, val = line.partition(":")
        key, val = key.strip(), val.split("#")[0].strip()
        if key == "depends_on":
            fm[key] = [d.strip() for d in val.strip("[]").split(",") if d.strip()]
        elif key in ("epic", "priority"):
            fm[key] = int(val)
        elif key == "capstone":
            fm[key] = val.lower() == "true"
        else:
            fm[key] = val
    return fm


def main() -> int:
    tasks = []
    for path in sorted(TASKS.glob("epic-*/E*-T*.md")):
        fm = parse_frontmatter(path)
        if fm is None or "id" not in fm:
            print(f"warning: skipping {path} (bad frontmatter)", file=sys.stderr)
            continue
        tasks.append(fm)

    if not tasks:
        print("no tasks found", file=sys.stderr)
        return 1

    tasks.sort(key=lambda t: t.get("priority", 9999))

    ids = {t["id"] for t in tasks}
    dupes = len(tasks) - len(ids)
    if dupes:
        print(f"warning: {dupes} duplicate task id(s)", file=sys.stderr)

    verified = {t["id"] for t in tasks if t.get("status") == "verified"}
    capstone_verified = {
        f"E{t['epic']}" for t in tasks if t.get("capstone") and t.get("status") == "verified"
    }
    done_refs = verified | capstone_verified

    def eligible(t: dict) -> bool:
        if t.get("status") not in ("pending", "refuted"):
            return False
        return all(dep in done_refs for dep in t.get("depends_on", []))

    next_up = [t for t in tasks if eligible(t)][:10]
    n_verified = len(verified)

    lines = [
        "# wasm-vm Priority Queue",
        "",
        "*Generated by `tools/build_queue.py` — do not edit by hand.*",
        "",
        f"**{n_verified} / {len(tasks)} tasks verified.**",
        "",
        "Legend: `[ ]` pending · `[~]` in-progress · `[?]` implemented (awaiting"
        " adversarial verification) · `[!]` refuted · `[x]` verified",
        "",
        "## Next up (deps satisfied, in priority order)",
        "",
    ]
    for t in next_up:
        lines.append(f"1. **{t['id']}** — {t.get('title', '?')}")
    lines.append("")

    current_epic = None
    for t in tasks:
        if t.get("epic") != current_epic:
            current_epic = t.get("epic")
            epic_dir = t["_path"].parent.name
            lines += ["", f"## Epic {current_epic} — `{epic_dir}`", ""]
        icon = STATUS_ICON.get(t.get("status", "pending"), " ")
        rel = t["_path"].relative_to(TASKS).as_posix()
        deps = ", ".join(t.get("depends_on", [])) or "—"
        cap = " **[CAPSTONE]**" if t.get("capstone") else ""
        lines.append(
            f"- [{icon}] `{t.get('priority', '?'):>3}` [{t['id']}]({rel})"
            f" — {t.get('title', '?')}{cap} *(deps: {deps})*"
        )

    QUEUE.write_text("\n".join(lines) + "\n", encoding="utf-8")
    print(f"wrote {QUEUE.relative_to(ROOT)}: {len(tasks)} tasks, {n_verified} verified")
    return 0


if __name__ == "__main__":
    sys.exit(main())
